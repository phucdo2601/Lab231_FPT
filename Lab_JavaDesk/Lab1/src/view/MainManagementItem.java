    public void loadSup() {
        cn = myLibrary.myConnection.makeConnection();
        try {
            if (cn != null) {
                Vector data = new Vector();
                Vector header = new Vector();
                Vector<Supplier> tmp = SuplierDAO.getAccounts();
                for (Supplier a : tmp) {
                    Vector row = new Vector();
                    row.add(a.getSupCode());
                    row.add(a.getSupName());
                    row.add(a.getAddress());
                    row.add(a.isCollaborating());
                    data.add(row);
                }
                header.add("Supplier Code");
                header.add("Supplier Name");
                header.add("Address");
                header.add("Collaborating");

                tableSup = (DefaultTableModel) tblSup.getModel();
                tableSup.setDataVector(data, header);
            }
        } catch (Exception e) {
            if (cn == null) {
                JOptionPane.showMessageDialog(jMenu1, "Sql Server dont connected!");
            }
            e.printStackTrace();
        } finally {
            try {
                if (cn != null) {
                    cn.close();
                }
            } catch (Exception e) {
                if (cn == null) {
                    JOptionPane.showMessageDialog(jMenu1, "Sql Server dont connected!");
                }
                e.printStackTrace();
            }
        }
    }

    public void loadSupRole() {
        Vector<SupRolde> result = SuplierDAO.getRole();
        if (result != null && result.size() > 0) {
            DefaultComboBoxModel model = (DefaultComboBoxModel) cbxSup.getModel();
            model.removeAllElements();
            model.addElement("");
            for (SupRolde r : result) {
                model.addElement(r.getSupCode() + "-" + r.getSupName());
            }
        }
    }

    public void loadItem() {
        cn = myLibrary.myConnection.makeConnection();
        try {
            if (cn != null) {
                Vector data = new Vector();
                Vector header = new Vector();
                Vector<Item> tmp = ItemDAO.getAllItems();
                for (Item a : tmp) {
                    Vector row = new Vector();
                    row.add(a.getItemCode());
                    row.add(a.getItemName());
                    row.add(a.getSupCode());
                    row.add(a.getUnit());
                    row.add(a.getPrice());
                    row.add(a.isSupplying());
                    data.add(row);
                }

                header.add("Item Code");
                header.add("Item Name");
                header.add("Suplier");
                header.add("Unit");
                header.add("Price");
                header.add("Supply");

                tableItem = (DefaultTableModel) tblItem.getModel();
                tableItem.setDataVector(data, header);
            }
        } catch (Exception e) {
            if (cn == null) {
                JOptionPane.showMessageDialog(jMenu1, "Sql Server dont connected!");
            }
            e.printStackTrace();
        } finally {
            try {
                if (cn != null) {
                    cn.close();
                }
            } catch (Exception e) {
                if (cn == null) {
                    JOptionPane.showMessageDialog(jMenu1, "Sql Server dont connected!");
                }
                e.printStackTrace();
            }
        }
    }

