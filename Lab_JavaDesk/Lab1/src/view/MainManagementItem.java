    public Item getmodelItem() {
        Item it = new Item();
        it.setItemCode(txtCodeItem.getText());
        it.setItemName(txtNameItem.getText());
        it.setPrice(Double.parseDouble(txtPriceItem.getText()));
        it.setUnit(txtUnitItem.getText());
        String tmp = "" + cbxSup.getSelectedItem();
        String supID = tmp.split("-")[0];
        it.setSupCode(supID);
        boolean isSupply = false;
        if (cbxSupItem.isSelected()) {
            isSupply = true;
        }
        it.setSupplying(isSupply);
        return it;
    }
